extension UIColor {
    static func boyBlue() -> UIColor {
        return UIColor(red:0/255, green:122/255, blue:255/255, alpha: 1.0)
    }
    
    static func lightBoyBlue() -> UIColor {
        return UIColor(red:97/255, green:174/255, blue:229/255, alpha: 1.0)
    }
    
    static func bickerPurple() -> UIColor {
        return UIColor(red:161/255, green:97/255, blue:209/255, alpha: 1.0)
    }
    
    static func girlPink() -> UIColor {
        return UIColor(red:255/255, green:103/255, blue:180/255, alpha: 1.0)
    }
    
    static func babyPink() -> UIColor {
        return UIColor(red:225/255, green:192/255, blue:229/255, alpha: 1.0)
    }
}
